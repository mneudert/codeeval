$fh = fopen($argv[1], 'r');

while (false !== ($line = fgets($fh))) {
    $chars  = str_split(trim($line));
    $output = array();

    foreach ($chars as $char) {
        if (!is_numeric($char)) {
            continue;
        }

        $output[] = $digits[$char];
    }

    for ($i = 0; $i < 6; $i++) {
        foreach ($output as $digit) {
            echo $digit[$i];
        }

        echo PHP_EOL;
    }
}
